#/usr/bin/perl
use strict;
use warnings;
my $oc = 0;
while (<>) {
    chomp;
    s/\/\*([^*][^\/]|.)*\*\///g;
    if (m/^([^\/][^*]|.)*\*\// && $oc == 1) { $oc = 0;}
    if (m/\/\*([^*][^\/]|.)*$/ && $oc == 0) { $oc = 1;}
    s/\/\*([^*][^\/]|.)*$//;
    s/^([^*][^\/]|.)*\*\///;
    s/\s*\/\/.*$//g;
    s/\s+//g;
    if ($oc == 0) {print "$_";}

}
print "\n";
